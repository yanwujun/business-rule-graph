class TestXLangSmoke:
    def test_exits_zero_no_bridges(self, cli_runner, python_only_project, monkeypatch):
        """x-lang exits 0 even when no cross-language bridges are detected."""
        monkeypatch.chdir(python_only_project)
        result = invoke_cli(cli_runner, ["x-lang"], cwd=python_only_project)
        assert result.exit_code == 0, f"x-lang exited non-zero:\n{result.output}"

    def test_exits_zero_with_proto_project(self, cli_runner, proto_python_project, monkeypatch):
        """x-lang exits 0 on a project with .proto files."""
        monkeypatch.chdir(proto_python_project)
        result = invoke_cli(cli_runner, ["x-lang"], cwd=proto_python_project)
        assert result.exit_code == 0, f"x-lang exited non-zero:\n{result.output}"


