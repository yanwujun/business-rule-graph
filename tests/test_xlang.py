class TestXLangJSON:
    def test_json_envelope_no_bridges(self, cli_runner, python_only_project, monkeypatch):
        monkeypatch.chdir(python_only_project)
        result = invoke_cli(cli_runner, ["x-lang"], cwd=python_only_project, json_mode=True)
        data = parse_json_output(result, "x-lang")
        assert_json_envelope(data, "x-lang")

    def test_json_summary_has_verdict(self, cli_runner, python_only_project, monkeypatch):
        monkeypatch.chdir(python_only_project)
        result = invoke_cli(cli_runner, ["x-lang"], cwd=python_only_project, json_mode=True)
        data = parse_json_output(result, "x-lang")
        summary = data["summary"]
        assert "verdict" in summary, f"summary missing 'verdict': {summary}"
        assert isinstance(summary["verdict"], str)
        assert summary["verdict"]

    def test_json_summary_has_bridges_count(self, cli_runner, python_only_project, monkeypatch):
        monkeypatch.chdir(python_only_project)
        result = invoke_cli(cli_runner, ["x-lang"], cwd=python_only_project, json_mode=True)
        data = parse_json_output(result, "x-lang")
        summary = data["summary"]
        assert "bridges" in summary, f"summary missing 'bridges': {summary}"
        assert isinstance(summary["bridges"], int)

    def test_json_summary_has_links_count(self, cli_runner, python_only_project, monkeypatch):
        monkeypatch.chdir(python_only_project)
        result = invoke_cli(cli_runner, ["x-lang"], cwd=python_only_project, json_mode=True)
        data = parse_json_output(result, "x-lang")
        summary = data["summary"]
        assert "links" in summary, f"summary missing 'links': {summary}"
        assert isinstance(summary["links"], int)

    def test_json_has_bridges_list(self, cli_runner, python_only_project, monkeypatch):
        monkeypatch.chdir(python_only_project)
        result = invoke_cli(cli_runner, ["x-lang"], cwd=python_only_project, json_mode=True)
        data = parse_json_output(result, "x-lang")
        assert "bridges" in data, f"Top-level 'bridges' key missing from envelope: {list(data)}"
        assert isinstance(data["bridges"], list)

