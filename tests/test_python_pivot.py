# ---------------------------------------------------------------------------
# is_async + decorators on Python symbols
# ---------------------------------------------------------------------------


def _extract(source: bytes) -> list[dict]:
    parser = get_parser("python")
    tree = parser.parse(source)
    return PythonExtractor().extract_symbols(tree, source, "test.py")


class TestAsyncDetection:
    """``async def`` should set ``is_async=True`` on the symbol."""

    def test_async_function_marked_async(self):
        symbols = _extract(b"async def cook(x): return x\n")
        cook = next(s for s in symbols if s["name"] == "cook")
        assert cook["is_async"] is True

    def test_sync_function_not_marked_async(self):
        symbols = _extract(b"def sync_fn(x): return x\n")
        sym = next(s for s in symbols if s["name"] == "sync_fn")
        assert sym["is_async"] is False

    def test_async_method_in_class_marked_async(self):
        source = b"class A:\n    async def run(self): pass\n"
        symbols = _extract(source)
        run = next(s for s in symbols if s["name"] == "run")
        assert run["kind"] == "method"
        assert run["is_async"] is True

    def test_decorated_async_method_marked_async(self):
        """A decorated ``async def`` is wrapped in decorated_definition;
        the async detection must drill into the inner function."""
        source = b"import functools\n@functools.wraps(None)\nasync def cached(): pass\n"
        symbols = _extract(source)
        cached = next(s for s in symbols if s["name"] == "cached")
        assert cached["is_async"] is True
        assert "@functools.wraps" in cached.get("decorators", "")


class TestDecoratorExtraction:
    """Decorators above ``def`` / ``class`` get joined into the
    ``decorators`` field comma-separated."""

    def test_property_decorator_captured(self):
        source = b"class A:\n    @property\n    def x(self): return 1\n"
        symbols = _extract(source)
        x = next(s for s in symbols if s["name"] == "x")
        assert "@property" in x.get("decorators", "")

    def test_pytest_fixture_decorator_captured(self):
        source = b"import pytest\n@pytest.fixture\ndef client(): pass\n"
        symbols = _extract(source)
        client = next(s for s in symbols if s["name"] == "client")
        assert "@pytest.fixture" in client.get("decorators", "")

    def test_undecorated_has_empty_decorators(self):
        source = b"def plain(): pass\n"
        symbols = _extract(source)
        plain = next(s for s in symbols if s["name"] == "plain")
        assert plain.get("decorators", "") == ""


