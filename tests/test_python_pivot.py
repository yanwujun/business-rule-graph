class TestPythonIdiomRegexes:
    """Sanity-check the regex patterns directly without spinning up a DB."""

    def test_mutable_default_list_matches(self):
        from roam.catalog.python_idioms import _MUTABLE_DEFAULT_RE

        assert _MUTABLE_DEFAULT_RE.search("def f(x=[])") is not None
        assert _MUTABLE_DEFAULT_RE.search("def g(d={})") is not None
        assert _MUTABLE_DEFAULT_RE.search("def h(x=list())") is not None
        assert _MUTABLE_DEFAULT_RE.search("def k(s=set())") is not None

    def test_immutable_default_does_not_match(self):
        from roam.catalog.python_idioms import _MUTABLE_DEFAULT_RE

        assert _MUTABLE_DEFAULT_RE.search("def f(x=None)") is None
        assert _MUTABLE_DEFAULT_RE.search('def g(s="")') is None
        assert _MUTABLE_DEFAULT_RE.search("def h(n=0)") is None
        assert _MUTABLE_DEFAULT_RE.search("def k(t=())") is None

    def test_bare_except_matches(self):
        from roam.catalog.python_idioms import _BARE_EXCEPT_RE

        assert _BARE_EXCEPT_RE.search("try:\n    x()\nexcept:\n    pass\n") is not None
        # Indented bare except inside a function
        assert _BARE_EXCEPT_RE.search("def f():\n    try:\n        x()\n    except:\n        pass\n") is not None

    def test_typed_except_does_not_match(self):
        from roam.catalog.python_idioms import _BARE_EXCEPT_RE

        assert _BARE_EXCEPT_RE.search("try:\n    x()\nexcept ValueError:\n    pass\n") is None
        assert _BARE_EXCEPT_RE.search("try:\n    x()\nexcept (A, B) as e:\n    pass\n") is None

    def test_none_eq_matches(self):
        from roam.catalog.python_idioms import _NONE_EQ_RE

        assert _NONE_EQ_RE.search("if x == None:") is not None
        assert _NONE_EQ_RE.search("if x != None:") is not None

    def test_none_is_does_not_match(self):
        from roam.catalog.python_idioms import _NONE_EQ_RE

        assert _NONE_EQ_RE.search("if x is None:") is None

    def test_logger_fstring_matches(self):
        from roam.catalog.python_idioms import _LOGGER_FSTRING_RE

        assert _LOGGER_FSTRING_RE.search('logger.info(f"x={x}")') is not None
        assert _LOGGER_FSTRING_RE.search('log.warning(f"oops")') is not None
        assert _LOGGER_FSTRING_RE.search("logging.debug(f'fmt')") is not None
        assert _LOGGER_FSTRING_RE.search('self.logger.error(f"x")') is not None

    def test_logger_lazy_format_does_not_match(self):
        from roam.catalog.python_idioms import _LOGGER_FSTRING_RE

        assert _LOGGER_FSTRING_RE.search('logger.info("x=%s", x)') is None
        assert _LOGGER_FSTRING_RE.search('logger.info("plain")') is None
