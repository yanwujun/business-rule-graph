from roam.commands.cmd_verify import SEVERITY_WARN, _check_claims


def test_check_claims_flags_specific_unsupported_claims(tmp_path: Path):
    doc = tmp_path / "README.md"
    doc.write_text(
        "\n".join(
            [
                "Roam is the fastest local code intelligence tool.",
                "It has 267 commands and 243 tools.",
            ]
        ),
        encoding="utf-8",
    )

    result = _check_claims(["README.md"], tmp_path)

    assert result["advisory"] is True
    assert result["claims_checked"] == 2
    assert [v["severity"] for v in result["violations"]] == [SEVERITY_WARN, SEVERITY_WARN]
    assert all(v["category"] == "claims" for v in result["violations"])


