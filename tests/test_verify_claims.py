def test_verify_claims_json_discloses_non_code_scope(tmp_path: Path, monkeypatch):
    repo = tmp_path / "repo"
    repo.mkdir()
    (repo / ".gitignore").write_text(".roam/\n", encoding="utf-8")
    (repo / "README.md").write_text("Roam is the fastest local code intelligence tool.\n", encoding="utf-8")
    git_init(repo)
    monkeypatch.chdir(repo)
    out, rc = index_in_process(repo, "--force")
    assert rc == 0, out

    result = invoke_cli(CliRunner(), ["verify", "--checks", "claims", "README.md"], cwd=repo, json_mode=True)
    envelope = parse_json_output(result)

    assert result.exit_code == 0
    assert envelope["summary"]["verdict"] == "PASS"
    assert envelope["summary"]["scope"]["non_code_file_count"] == 1
    assert envelope["categories"]["claims"]["violation_count"] == 1


def test_verify_report_includes_claim_surfaces_by_default(tmp_path: Path, monkeypatch):
    repo = tmp_path / "repo"
    repo.mkdir()
    (repo / ".gitignore").write_text(".roam/\n", encoding="utf-8")
